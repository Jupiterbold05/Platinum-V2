// Helper conversion function that uses a mapping object.
function convertText(mapping, text) {
  let result = "";
  for (const ch of text) {
    // If the mapping has an entry for the character, use it; otherwise, use the original character.
    result += mapping[ch] !== undefined ? mapping[ch] : ch;
  }
  return result;
}

